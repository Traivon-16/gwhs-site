
function divide(x, y)
{
	return (x/y);
}

function multiply(x, y)
{
    return x*y;
}
function add(x, y)
{
    return x+y
}
function power(x, y)
{
	return x^y;
}
function square_root(x)
{
	return Math.sqrt(x);
}
function logbase10(x)
{
	return Math.log10(x);
}
function subtract(x, y)
{
   return (x-y);
}
function generateRandomNumber()
{
	return Math.floor(Math.random() * (999 - 0 + 1)) + 1;
}
